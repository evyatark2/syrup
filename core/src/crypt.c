{
    return context->iv;
}

static void crypt(uint8_t *iv, size_t length, uint8_t *data)
{
    size_t start = 0;
    size_t llength = 0x5B0;

    while (length > 0) {
        uint8_t my_iv[16];
        for (int i = 0; i < 16; i++)
            my_iv[i] = iv[i%4];

        if (length < llength)
            llength = length;

        for (int i = start; i < start + llength; i++) {
            if ((i - start) % 16 == 0)
                AES_ECB_encrypt(&CONTEXT, my_iv);

            data[i] ^= my_iv[(i - start) % 16];
        }

        start += llength;
        length -= llength;
        llength = 0x5B4;
    }

    // update IV
    uint8_t new_iv[] = { 0xF2, 0x53, 0x50, 0xC6 };
    for (int i = 0; i < 4; i++) {
        new_iv[0] += FUNNY_BYTES[new_iv[1]] - iv[i];
        new_iv[1] -= new_iv[2] ^ FUNNY_BYTES[iv[i]];
        new_iv[2] ^= FUNNY_BYTES[new_iv[3]] + iv[i];
        new_iv[3] -= new_iv[0] - FUNNY_BYTES[iv[i]];
        uint32_t merry = new_iv[0] |
            (uint32_t)new_iv[1] << 8 |
            (uint32_t)new_iv[2] << 16 |
            (uint32_t)new_iv[3] << 24;
        merry = (merry >> 29) | (merry << 3);
        new_iv[0] = merry;
        new_iv[1] = merry >> 8;
        new_iv[2] = merry >> 16;
        new_iv[3] = merry >> 24;
    }

    memcpy(iv, new_iv, 4);
}

static uint8_t roll_left(uint8_t in, uint8_t count) {
    return (in << count) | (in >> (count - 8));
}

static uint8_t roll_right(uint8_t in, uint8_t count) {
    return (in << (count - 8)) | (in >> count);
}

